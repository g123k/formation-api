List<IngredientTranslation> ingredientsTranslations(String id) {
  var translations = _ingredientsTranslations[id];
  if (translations != null) {
    return translations;
  } else {
    id = id.split(':')[1];

    // Additive
    if (id.startsWith(RegExp('[eE][0-9]{3,4}'))) {
      var additive = additivesList[id];

      if (additive != null) {
        return [
          IngredientTranslation('en', id),
          IngredientTranslation('fr', additive),
        ];
      } else {
        return [
          IngredientTranslation('en', id),
          IngredientTranslation('fr', id),
        ];
      }
      // Unknown product
    } else {
      id = id.replaceAll('-', '');

      return [
        IngredientTranslation('en', id),
        IngredientTranslation('fr', id),
      ];
    }
  }
}

IngredientTranslation? findIngredientTranslation(
    List<IngredientTranslation>? list, String? language) {
  if (list != null && language != null) {
    for (var translation in list) {
      if (translation.language == language) {
        return translation;
      }
    }
  }

  return null;
}

class IngredientTranslation {
  final String language;
  final String value;

  const IngredientTranslation(this.language, this.value);

  Map<String, String> toJson() => {'language': language, 'text': value};
}
